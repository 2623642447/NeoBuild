import { useRef, useState } from 'react'
import { useBuildStore } from '@/lib/store'
import { formatPrice } from '@/lib/utils'
import { Button } from '@/components/ui/button'
import { Dialog, DialogContent, DialogHeader, DialogTitle } from '@/components/ui/dialog'
import { Toast, useToast } from '@/components/ui/toast'
import { toPng } from 'html-to-image'
import { FileText, Copy, Download } from 'lucide-react'

interface ExportPanelProps {
  buildId: string
  open: boolean
  onOpenChange: (open: boolean) => void
}

export function ExportPanel({ buildId, open, onOpenChange }: ExportPanelProps) {
  const { builds } = useBuildStore()
  const { toast, showToast } = useToast()
  const exportRef = useRef<HTMLDivElement>(null)
  const [exporting, setExporting] = useState(false)

  const build = builds.find(b => b.id === buildId)
  if (!build) return null

  const total = build.categories.reduce(
    (sum, cat) => sum + cat.items.reduce((s, item) => s + item.price, 0),
    0
  )

  const filledCategories = build.categories.filter(c => c.items.length > 0)

  const generateTextList = () => {
    const lines: string[] = []
    lines.push(`🔧 ${build.name}`)
    lines.push('━'.repeat(30))
    for (const cat of filledCategories) {
      const catTotal = cat.items.reduce((s, i) => s + i.price, 0)
      lines.push(``)
      lines.push(`【${cat.name}】`)
      for (const item of cat.items) {
        lines.push(`  ${item.name} - ¥${item.price}${item.link ? ` (${item.platform || '链接'})` : ''}`)
      }
      if (cat.items.length > 1) {
        lines.push(`  小计: ¥${catTotal.toFixed(2)}`)
      }
    }
    lines.push('')
    lines.push('━'.repeat(30))
    lines.push(`💰 总计: ¥${total.toFixed(2)}`)
    lines.push('')
    lines.push(' Generated by NeoBuild 装机模拟器')
    return lines.join('\n')
  }

  const handleCopyText = async () => {
    const text = generateTextList()
    try {
      await navigator.clipboard.writeText(text)
      showToast('文本清单已复制到剪贴板', 'success')
    } catch {
      showToast('复制失败，请手动复制', 'error')
    }
  }

  const handleExportImage = async () => {
    if (!exportRef.current) return
    setExporting(true)
    try {
      const dataUrl = await toPng(exportRef.current, {
        backgroundColor: '#ffffff',
        pixelRatio: 2,
      })
      const link = document.createElement('a')
      link.download = `${build.name}-配置单.png`
      link.href = dataUrl
      link.click()
      showToast('配置单图片已下载', 'success')
    } catch {
      showToast('导出图片失败', 'error')
    }
    setExporting(false)
  }

  return (
    <>
      <Dialog open={open} onOpenChange={onOpenChange}>
        <DialogContent className="max-w-2xl max-h-[85vh] overflow-y-auto scrollbar-thin">
          <DialogHeader>
            <DialogTitle>导出配置单</DialogTitle>
          </DialogHeader>

          {/* Action buttons */}
          <div className="flex gap-2 mb-4">
            <Button variant="cyan" onClick={handleExportImage} disabled={exporting}>
              <Download className="h-4 w-4 mr-2" />
              {exporting ? '导出中...' : '导出图片'}
            </Button>
            <Button variant="glass" onClick={handleCopyText}>
              <Copy className="h-4 w-4 mr-2" />
              复制文本
            </Button>
          </div>

          {/* Preview - Image export target */}
          <div
            ref={exportRef}
            className="rounded-xl p-6 space-y-4"
            style={{
              background: 'linear-gradient(135deg, #ffffff 0%, #f8fafc 50%, #ffffff 100%)',
            }}
          >
            {/* Header */}
            <div style={{ textAlign: 'center', marginBottom: '24px' }}>
              <h2 style={{
                fontSize: '24px',
                fontWeight: 700,
                color: '#3B82F6',
                fontFamily: 'Inter, sans-serif',
                letterSpacing: '-0.02em',
              }}>
                {build.name}
              </h2>
              <div style={{
                height: '2px',
                background: 'linear-gradient(90deg, transparent, #3B82F6, transparent)',
                margin: '12px auto',
                width: '60%',
              }} />
            </div>

            {/* Component List */}
            {filledCategories.map(cat => {
              const catTotal = cat.items.reduce((s, i) => s + i.price, 0)
              return (
                <div key={cat.id} style={{ marginBottom: '16px' }}>
                  <div style={{
                    display: 'flex',
                    justifyContent: 'space-between',
                    alignItems: 'center',
                    padding: '8px 12px',
                    background: 'rgba(59, 130, 246, 0.06)',
                    borderRadius: '8px',
                    borderLeft: '3px solid #3B82F6',
                  }}>
                    <span style={{
                      color: '#3B82F6',
                      fontWeight: 600,
                      fontSize: '14px',
                      fontFamily: 'Inter, sans-serif',
                    }}>
                      {cat.name}
                    </span>
                    {cat.items.length > 1 && (
                      <span style={{
                        color: '#EA580C',
                        fontWeight: 600,
                        fontSize: '13px',
                        fontFamily: 'JetBrains Mono, monospace',
                      }}>
                        ¥{catTotal.toFixed(2)}
                      </span>
                    )}
                  </div>
                  {cat.items.map(item => (
                    <div key={item.id} style={{
                      display: 'flex',
                      justifyContent: 'space-between',
                      alignItems: 'center',
                      padding: '6px 16px',
                      color: '#1e293b',
                      fontSize: '13px',
                    }}>
                      <span style={{ fontFamily: 'Inter, sans-serif' }}>{item.name}</span>
                      <span style={{
                        color: '#EA580C',
                        fontWeight: 600,
                        fontFamily: 'JetBrains Mono, monospace',
                      }}>
                        ¥{item.price.toFixed(2)}
                      </span>
                    </div>
                  ))}
                </div>
              )
            })}

            {/* Total */}
            <div style={{
              borderTop: '1px solid rgba(59, 130, 246, 0.2)',
              paddingTop: '16px',
              display: 'flex',
              justifyContent: 'space-between',
              alignItems: 'center',
            }}>
              <span style={{
                color: '#64748b',
                fontSize: '16px',
                fontWeight: 600,
                fontFamily: 'Inter, sans-serif',
              }}>
                总计
              </span>
              <span style={{
                color: '#3B82F6',
                fontSize: '28px',
                fontWeight: 700,
                fontFamily: 'JetBrains Mono, monospace',
              }}>
                ¥{total.toFixed(2)}
              </span>
            </div>

            {/* Footer */}
            <div style={{
              textAlign: 'center',
              color: '#94a3b8',
              fontSize: '11px',
              paddingTop: '12px',
            }}>
              Generated by NeoBuild 装机模拟器
            </div>
          </div>

          {/* Text preview */}
          <div className="mt-4">
            <div className="flex items-center gap-2 mb-2">
              <FileText className="h-4 w-4 text-muted-foreground" />
              <span className="text-sm font-medium text-muted-foreground">文本预览</span>
            </div>
            <pre className="bg-surface rounded-lg p-4 text-xs text-muted-foreground whitespace-pre-wrap overflow-x-auto scrollbar-thin font-mono max-h-48">
              {generateTextList()}
            </pre>
          </div>
        </DialogContent>
      </Dialog>

      <Toast {...toast} />
    </>
  )
}
